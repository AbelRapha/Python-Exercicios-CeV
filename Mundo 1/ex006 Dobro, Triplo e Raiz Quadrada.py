num = int(input("Digite um numero "))

def Dobro(num):
    return num*2

def Triplo(num):
    return num*3

def sqrt(num):
    return num**1/2

print(f"O valor que voce digitou foi o {num}. \n E o dobro desse numero e {Dobro(num)}, \n o triplo desse valor e {Triplo(num)} \n e por fim a raiz desse numero e {sqrt(num)})")